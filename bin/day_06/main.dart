int updateInternalTimers(List<int> lanternFish, [int days = 80]) {
  var day = 0;

  while (day < days) {
    final List<int> newLanternFish = [];

    for (var i = 0; i < lanternFish.length; i++) {
      final fish = lanternFish[i];

      if (fish == 0) {
        lanternFish[i] = 6;
        newLanternFish.add(8);
      } else {
        lanternFish[i] = fish - 1;
      }
    }
    lanternFish.addAll(newLanternFish);

    day++;
  }
  return lanternFish.length;
}

